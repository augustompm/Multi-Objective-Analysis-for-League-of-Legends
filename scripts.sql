CREATE TABLE `matches` (
  `match_id` varchar(20) NOT NULL,
  `player_summoner_id` varchar(100) NOT NULL,
  `player_champion` varchar(50) DEFAULT NULL,
  `player_won` int DEFAULT NULL,
  `player_team` varchar(10) DEFAULT NULL,
  `player_lane` varchar(10) DEFAULT NULL,
  `player_role` varchar(20) DEFAULT NULL,
  `player_position` varchar(20) DEFAULT NULL,
  `game_duration` int DEFAULT NULL,
  `game_mode` varchar(50) DEFAULT NULL,
  `game_start_timestamp` bigint DEFAULT NULL,
  `team1_won` int DEFAULT NULL,
  `player_kills` int DEFAULT NULL,
  `player_deaths` int DEFAULT NULL,
  `player_assists` int DEFAULT NULL,
  `team1_champion1` varchar(50) DEFAULT NULL,
  `team1_champion2` varchar(50) DEFAULT NULL,
  `team1_champion3` varchar(50) DEFAULT NULL,
  `team1_champion4` varchar(50) DEFAULT NULL,
  `team1_champion5` varchar(50) DEFAULT NULL,
  `team2_champion1` varchar(50) DEFAULT NULL,
  `team2_champion2` varchar(50) DEFAULT NULL,
  `team2_champion3` varchar(50) DEFAULT NULL,
  `team2_champion4` varchar(50) DEFAULT NULL,
  `team2_champion5` varchar(50) DEFAULT NULL,
  PRIMARY KEY (`match_id`,`player_summoner_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

-- 1 - champion_performance.csv:

SELECT
    player_champion,
    COUNT(*) AS total_matches,
    SUM(player_won) AS total_wins,
    SUM(player_won) / COUNT(*) AS win_rate
FROM
    matches
GROUP BY
    player_champion
order BY 4

