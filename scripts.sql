--- 3- adjusted Select * 

SELECT
    match_id,
    player_summoner_id,
    player_champion,
    player_won as binary_win,
    CASE 
        WHEN player_won = 1 THEN 'Yes'
        WHEN player_won = 0 THEN 'No'
        ELSE 'Unknown'
    END AS win,  
    player_team,
    player_lane,
    player_role,
    player_position,
    game_duration,
    game_mode,
    FROM_UNIXTIME(
        CAST(
            REPLACE(game_start_timestamp, ',', '') AS UNSIGNED
        ) / 1000  
    ) AS game_start_datetime,  
    team1_won,
    player_kills,
    player_deaths,
    player_assists,
    team1_champion1,
    team1_champion2,
    team1_champion3,
    team1_champion4,
    team1_champion5,
    team2_champion1,
    team2_champion2,
    team2_champion3,
    team2_champion4,
    team2_champion5
FROM matches